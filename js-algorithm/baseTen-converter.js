 function baseConverter(base, number) {
    return number.toString(base)
 }

 console.log("############ BASE 10 ###############")
console.log(baseConverter(10, 2))
console.log(baseConverter(10, 12))
console.log(baseConverter(10, 89))
console.log("############ BASE 16 ###############")
console.log(baseConverter(16, 2))
console.log(baseConverter(16, 12))
console.log(baseConverter(16, 89))


